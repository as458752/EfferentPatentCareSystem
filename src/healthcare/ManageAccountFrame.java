    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        String pass = user.getPassword();
        String oldPass = txtOldPass.getText();
        String password1 = txtPass1.getText();
        String password2 = txtPass2.getText();
        if(!pass.equals(oldPass)) JOptionPane.showMessageDialog(this, "Password is incorrect, please re-enter the password.");
        else if(password1.equals("") || password2.equals("")) JOptionPane.showMessageDialog(this, "Please enter the new password.");
        else if(!password1.equals(password2)) JOptionPane.showMessageDialog(this, "New Password do not match.");
        else
        {
            user.setPassword(password1);
            DataAccessor.storeUser(user);
            this.setVisible(false);
        }
    }//GEN-LAST:event_btnOKActionPerformed


